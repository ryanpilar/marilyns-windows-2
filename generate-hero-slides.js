const { promises: fs } = require("fs");
const path = require("path");
const { createClient } = require("contentful");

require("dotenv").config();

const outputPath = path.join(
  __dirname,
  "src",
  "generated",
  "heroSlides.json"
);
const edgePreloadOutputPath = path.join(
  __dirname,
  "netlify",
  "edge-functions",
  "generated-hero-preload.js"
);
const cloudinaryVersionSegment = /\/v\d+\//;
const heroImageWidths = [640, 960, 1280, 1600, 1920, 2560];

const buildHeroImageUrl = (url, width) => {
  if (!url || !width || !url.includes("res.cloudinary.com")) {
    return url;
  }

  const versionSegment = url.match(cloudinaryVersionSegment);
  if (!versionSegment || versionSegment.index === undefined) {
    return url;
  }

  const versionIndex = versionSegment.index;
  const transform = `c_limit,w_${width},f_auto,q_auto`;

  return `${url.slice(0, versionIndex)}/${transform}${url.slice(versionIndex)}`;
};

const writeEdgePreloadModule = async (slides) => {
  const firstSlide = slides?.[0];
  if (!firstSlide?.src) {
    throw new Error("No first hero slide is available for preloading.");
  }

  const href = buildHeroImageUrl(firstSlide.src, 1920);
  const srcSet = heroImageWidths
    .map((width) => `${buildHeroImageUrl(firstSlide.src, width)} ${width}w`)
    .join(", ");
  const moduleSource = `// Generated by generate-hero-slides.js.\nexport const HERO_PRELOAD_HREF = ${JSON.stringify(
    href
  )};\nexport const HERO_PRELOAD_SRCSET = ${JSON.stringify(srcSet)};\n`;

  await fs.mkdir(path.dirname(edgePreloadOutputPath), { recursive: true });
  await fs.writeFile(edgePreloadOutputPath, moduleSource, "utf8");
};

const cleanText = (value) =>
  typeof value === "string" ? value.trim() : "";

const usableImageDescription = (value) => {
  const text = cleanText(value);
  return text.length >= 8 ? text : "";
};

const createContentfulClient = () => {
  const { REACT_APP_CONTENTFUL_SPACE, REACT_APP_CONTENTFUL_TOKEN } =
    process.env;

  if (!REACT_APP_CONTENTFUL_SPACE || !REACT_APP_CONTENTFUL_TOKEN) {
    throw new Error(
      "Missing Contentful env vars: REACT_APP_CONTENTFUL_SPACE and/or REACT_APP_CONTENTFUL_TOKEN"
    );
  }

  return createClient({
    space: REACT_APP_CONTENTFUL_SPACE,
    accessToken: REACT_APP_CONTENTFUL_TOKEN,
  });
};

const serializeSlide = (entry) => {
  const image = entry.fields.cloudinaryImage?.[0];

  if (!image?.secure_url) {
    return null;
  }

  const context = image.context?.custom || {};
  const alt =
    usableImageDescription(context.alt) ||
    "Marilyn's Windows custom window treatments";

  return {
    id: entry.sys.id,
    updatedAt: entry.sys.updatedAt,
    src: image.secure_url,
    width: image.width || 2000,
    height: image.height || 1200,
    alt,
    caption: usableImageDescription(context.caption),
    dataPin: usableImageDescription(context.dataPin),
  };
};

async function generateHeroSlides() {
  const client = createContentfulClient();
  const entries = await client.getEntries({ content_type: "slider" });
  const slides = entries.items.map(serializeSlide).filter(Boolean);

  if (!slides.length) {
    throw new Error("Contentful returned no usable slider images.");
  }

  await fs.mkdir(path.dirname(outputPath), { recursive: true });
  await fs.writeFile(
    outputPath,
    `${JSON.stringify({ slides }, null, 2)}\n`,
    "utf8"
  );
  await writeEdgePreloadModule(slides);

  console.log(`Generated ${slides.length} hero slides at ${outputPath}`);
}

generateHeroSlides().catch(async (error) => {
  try {
    await fs.access(outputPath);
    const existingSlides = JSON.parse(await fs.readFile(outputPath, "utf8"));
    await writeEdgePreloadModule(existingSlides.slides);
    console.warn(
      `Hero slide refresh failed; using the existing generated data. ${error.message}`
    );
  } catch (accessError) {
    console.error("Hero slide generation failed.", error);
    process.exitCode = 1;
  }
});
